x = [51,10,15,25,26,30,35,40,11,23,45,68]
print(x[5]) #dihitung dari kiri -->, dimulai index 0
print(x[-3]) #dihitung dari kanan <--, dimulai index 1
print(x[2:6]) #kiri dihitung batas dan diakhiri dengan batas sebelum akhir.
print(x[:9]) #menghitung menggunakan index 1 dari kiri tergantung pembandingnya.
print(x[1:7:2]) #membuat list dari anggota elemen List x dengan index 1 hingga sebelum index 7, dengan "step" 2 (dalam hal ini hanya index 1, 3, 5).

