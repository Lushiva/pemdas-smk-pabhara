# pertambahan
print("PERTAMBAHAN")
tambah = 10 + 5
print("10 + 5 =", tambah)

# pengurangan
print("PENGURANGAN")
kurang = 10 - 5
print("10 - 5 =", kurang)

# pembagian
print("PEMBAGIAN")
bagi = 10 / 5
print("10 / 5 =", bagi)

# perkalian
print("PERKALIAN")
kali = 10 * 2
print("10 * 2 =", kali)

# modulus atau sisa bagi
print("MODULUS")
modulus1 = 10 % 5
modulus2 = 10 % 4
print("10 % 5 =", modulus1) # ini hasilnya 0 karena tidak ada sisa
print("10 % 4 =", modulus2) # ini hasilnya 2 karena ada angka yang masih sisa tidak terbagi

# eksponen atau perpangkatan
print("EKSPONEN")
eksponen1 = 10 ** 5
eksponen2 = 5 ** 3
print("10 ** 5=", eksponen1) # 10 pangkat 5 maka 10x10x10x10x10 = ?
print("5 ** 3=", eksponen2) # 5 pangkat 3 maka 5x5x5 = ?
